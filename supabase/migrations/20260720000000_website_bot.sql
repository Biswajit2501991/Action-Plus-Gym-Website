-- Public: list active FAQs
CREATE OR REPLACE FUNCTION public.website_bot_list_faqs(p_gym_id uuid)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_rows jsonb;
BEGIN
  SELECT COALESCE(jsonb_agg(
    jsonb_build_object(
      'id', f.id,
      'question', f.question,
      'answer', f.answer,
      'sort_order', f.sort_order
    ) ORDER BY f.sort_order, f.id
  ), '[]'::jsonb)
  INTO v_rows
  FROM website_bot_faqs f
  WHERE f.gym_id = p_gym_id
    AND f.is_active = true;

  RETURN jsonb_build_object('ok', true, 'faqs', v_rows);
END;
$$;

-- Public: submit enquiry (creates thread + first customer message)
CREATE OR REPLACE FUNCTION public.website_bot_submit_enquiry(
  p_gym_id uuid,
  p_full_name text,
  p_mobile text,
  p_message text,
  p_email text DEFAULT NULL,
  p_public_token uuid DEFAULT NULL
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_thread website_bot_threads%ROWTYPE;
  v_token uuid;
BEGIN
  IF p_full_name IS NULL OR length(trim(p_full_name)) < 2 THEN
    RETURN jsonb_build_object('ok', false, 'error', 'Name required');
  END IF;
  IF p_mobile IS NULL OR length(trim(p_mobile)) < 6 THEN
    RETURN jsonb_build_object('ok', false, 'error', 'Mobile required');
  END IF;
  IF p_message IS NULL OR length(trim(p_message)) < 2 THEN
    RETURN jsonb_build_object('ok', false, 'error', 'Message required');
  END IF;

  v_token := COALESCE(p_public_token, gen_random_uuid());

  SELECT * INTO v_thread
  FROM website_bot_threads
  WHERE gym_id = p_gym_id
    AND public_token = v_token
  LIMIT 1;

  IF FOUND THEN
    UPDATE website_bot_threads SET
      customer_name = trim(p_full_name),
      mobile = trim(p_mobile),
      email = NULLIF(trim(COALESCE(p_email, '')), ''),
      status = 'open',
      updated_at = now()
    WHERE id = v_thread.id
    RETURNING * INTO v_thread;
  ELSE
    INSERT INTO website_bot_threads (
      gym_id, public_token, customer_name, mobile, email, status
    ) VALUES (
      p_gym_id,
      v_token,
      trim(p_full_name),
      trim(p_mobile),
      NULLIF(trim(COALESCE(p_email, '')), ''),
      'open'
    )
    RETURNING * INTO v_thread;
  END IF;

  INSERT INTO website_bot_messages (gym_id, thread_id, sender, body)
  VALUES (p_gym_id, v_thread.id, 'customer', trim(p_message));

  RETURN jsonb_build_object(
    'ok', true,
    'public_token', v_thread.public_token,
    'thread_id', v_thread.id
  );
END;
$$;

-- Public: get thread by token, or latest for mobile under same gym
CREATE OR REPLACE FUNCTION public.website_bot_get_thread(
  p_gym_id uuid,
  p_public_token uuid DEFAULT NULL,
  p_mobile text DEFAULT NULL
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_thread website_bot_threads%ROWTYPE;
  v_msgs jsonb;
BEGIN
  IF p_public_token IS NOT NULL THEN
    SELECT * INTO v_thread
    FROM website_bot_threads
    WHERE gym_id = p_gym_id
      AND public_token = p_public_token
    LIMIT 1;
  ELSIF p_mobile IS NOT NULL AND length(trim(p_mobile)) >= 6 THEN
    SELECT * INTO v_thread
    FROM website_bot_threads
    WHERE gym_id = p_gym_id
      AND mobile = trim(p_mobile)
    ORDER BY updated_at DESC
    LIMIT 1;
  ELSE
    RETURN jsonb_build_object('ok', false, 'error', 'Token or mobile required');
  END IF;

  IF NOT FOUND THEN
    RETURN jsonb_build_object('ok', false, 'error', 'Conversation not found');
  END IF;

  SELECT COALESCE(jsonb_agg(
    jsonb_build_object(
      'id', m.id,
      'sender', m.sender,
      'body', m.body,
      'staff_name', m.staff_name,
      'created_at', m.created_at
    ) ORDER BY m.created_at, m.id
  ), '[]'::jsonb)
  INTO v_msgs
  FROM website_bot_messages m
  WHERE m.thread_id = v_thread.id;

  RETURN jsonb_build_object(
    'ok', true,
    'thread', jsonb_build_object(
      'id', v_thread.id,
      'public_token', v_thread.public_token,
      'customer_name', v_thread.customer_name,
      'mobile', v_thread.mobile,
      'email', v_thread.email,
      'status', v_thread.status,
      'created_at', v_thread.created_at,
      'updated_at', v_thread.updated_at
    ),
    'messages', v_msgs
  );
END;
$$;

-- Admin: list threads
CREATE OR REPLACE FUNCTION public.website_bot_admin_list_threads(p_token text)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_staff record;
  v_rows jsonb;
BEGIN
  SELECT * INTO v_staff FROM website_session_staff(p_token);
  IF NOT FOUND THEN RETURN jsonb_build_object('ok', false, 'error', 'Unauthorized'); END IF;

  SELECT COALESCE(jsonb_agg(to_jsonb(t) ORDER BY
    CASE t.status WHEN 'open' THEN 0 WHEN 'answered' THEN 1 ELSE 2 END,
    t.updated_at DESC
  ), '[]'::jsonb)
  INTO v_rows
  FROM (
    SELECT
      th.id,
      th.public_token,
      th.customer_name,
      th.mobile,
      th.email,
      th.status,
      th.created_at,
      th.updated_at,
      (
        SELECT left(m.body, 120)
        FROM website_bot_messages m
        WHERE m.thread_id = th.id
        ORDER BY m.created_at DESC
        LIMIT 1
      ) AS last_message,
      (
        SELECT count(*)::int
        FROM website_bot_messages m
        WHERE m.thread_id = th.id AND m.sender = 'customer'
      ) AS customer_message_count
    FROM website_bot_threads th
    WHERE th.gym_id = v_staff.gym_id
    ORDER BY
      CASE th.status WHEN 'open' THEN 0 WHEN 'answered' THEN 1 ELSE 2 END,
      th.updated_at DESC
    LIMIT 200
  ) t;

  RETURN jsonb_build_object('ok', true, 'threads', v_rows);
END;
$$;

-- Admin: get one thread + messages
CREATE OR REPLACE FUNCTION public.website_bot_admin_get_thread(
  p_token text,
  p_thread_id bigint
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_staff record;
  v_thread website_bot_threads%ROWTYPE;
  v_msgs jsonb;
BEGIN
  SELECT * INTO v_staff FROM website_session_staff(p_token);
  IF NOT FOUND THEN RETURN jsonb_build_object('ok', false, 'error', 'Unauthorized'); END IF;

  SELECT * INTO v_thread
  FROM website_bot_threads
  WHERE id = p_thread_id
    AND gym_id = v_staff.gym_id
  LIMIT 1;

  IF NOT FOUND THEN
    RETURN jsonb_build_object('ok', false, 'error', 'Not found');
  END IF;

  SELECT COALESCE(jsonb_agg(
    jsonb_build_object(
      'id', m.id,
      'sender', m.sender,
      'body', m.body,
      'staff_name', m.staff_name,
      'created_at', m.created_at
    ) ORDER BY m.created_at, m.id
  ), '[]'::jsonb)
  INTO v_msgs
  FROM website_bot_messages m
  WHERE m.thread_id = v_thread.id;

  RETURN jsonb_build_object(
    'ok', true,
    'thread', to_jsonb(v_thread),
    'messages', v_msgs
  );
END;
$$;

-- Admin: reply as Action Plus Gym
CREATE OR REPLACE FUNCTION public.website_bot_admin_reply(
  p_token text,
  p_thread_id bigint,
  p_body text
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_staff record;
  v_thread website_bot_threads%ROWTYPE;
BEGIN
  SELECT * INTO v_staff FROM website_session_staff(p_token);
  IF NOT FOUND THEN RETURN jsonb_build_object('ok', false, 'error', 'Unauthorized'); END IF;

  IF p_body IS NULL OR length(trim(p_body)) < 1 THEN
    RETURN jsonb_build_object('ok', false, 'error', 'Reply required');
  END IF;

  SELECT * INTO v_thread
  FROM website_bot_threads
  WHERE id = p_thread_id
    AND gym_id = v_staff.gym_id
  LIMIT 1;

  IF NOT FOUND THEN
    RETURN jsonb_build_object('ok', false, 'error', 'Not found');
  END IF;

  INSERT INTO website_bot_messages (gym_id, thread_id, sender, body, staff_name)
  VALUES (
    v_staff.gym_id,
    v_thread.id,
    'staff',
    trim(p_body),
    COALESCE(NULLIF(trim(v_staff.full_name), ''), v_staff.staff_login_id, 'Action Plus Gym')
  );

  UPDATE website_bot_threads SET
    status = 'answered',
    updated_at = now()
  WHERE id = v_thread.id;

  RETURN jsonb_build_object('ok', true);
END;
$$;

-- Admin: list all FAQs (including inactive)
CREATE OR REPLACE FUNCTION public.website_bot_admin_list_faqs(p_token text)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_staff record;

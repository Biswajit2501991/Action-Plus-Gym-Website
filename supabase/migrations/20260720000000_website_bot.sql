-- Ask Me bot: FAQs + customer enquiry threads (isolated from visitors/leads).

CREATE TABLE IF NOT EXISTS public.website_bot_faqs (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  gym_id uuid NOT NULL REFERENCES public.gyms(id) ON DELETE CASCADE,
  question text NOT NULL,
  answer text NOT NULL,
  sort_order int NOT NULL DEFAULT 0,
  is_active boolean NOT NULL DEFAULT true,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_website_bot_faqs_gym
  ON public.website_bot_faqs (gym_id, sort_order);

CREATE TABLE IF NOT EXISTS public.website_bot_threads (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  gym_id uuid NOT NULL REFERENCES public.gyms(id) ON DELETE CASCADE,
  public_token uuid NOT NULL DEFAULT gen_random_uuid(),
  customer_name text NOT NULL,
  mobile text NOT NULL,
  email text,
  status text NOT NULL DEFAULT 'open'
    CHECK (status IN ('open', 'answered', 'closed')),
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  UNIQUE (gym_id, public_token)
);

CREATE INDEX IF NOT EXISTS idx_website_bot_threads_gym_updated
  ON public.website_bot_threads (gym_id, updated_at DESC);

CREATE INDEX IF NOT EXISTS idx_website_bot_threads_gym_mobile
  ON public.website_bot_threads (gym_id, mobile);

CREATE TABLE IF NOT EXISTS public.website_bot_messages (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  gym_id uuid NOT NULL REFERENCES public.gyms(id) ON DELETE CASCADE,
  thread_id bigint NOT NULL REFERENCES public.website_bot_threads(id) ON DELETE CASCADE,
  sender text NOT NULL CHECK (sender IN ('customer', 'staff', 'bot')),
  body text NOT NULL,
  staff_name text,
  created_at timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_website_bot_messages_thread
  ON public.website_bot_messages (thread_id, created_at);

ALTER TABLE public.website_bot_faqs ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.website_bot_threads ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.website_bot_messages ENABLE ROW LEVEL SECURITY;

